using UnityEngine;

public class CubeMoverPhases : MonoBehaviour
{
    float timer;

    void Update()
    {
        timer += Time.deltaTime;

        if (timer < 5f)
        {
            // ??????? 1: ????? ??? Y (?????? ??????? Mag)
            transform.Rotate(0f, 100f * Time.deltaTime, 0f);
        }
        else if (timer < 10f)
        {
            // ??????? 2: ????? ?? ????? ???? (?????? ?????? Accel)
            // ?? ????? ????? ?????? ?????? ?????? ???? ???? ????? ??? 4 ???
            if (timer < 6.2f) transform.rotation = Quaternion.Euler(45, 0, 0);
            else if (timer < 7.4f) transform.rotation = Quaternion.Euler(-45, 0, 0);
            else if (timer < 8.6f) transform.rotation = Quaternion.Euler(0, 0, 45);
            else transform.rotation = Quaternion.Euler(0, 0, -45);
        }
        else if (timer < 13f)
        {
            // ??????? 3: ???? ??? (?????? ??????? Gyro)
            // ????? ????? ?????? ??????
            transform.rotation = Quaternion.identity;
        }
    }
}